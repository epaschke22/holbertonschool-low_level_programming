#include "3-calc.h"

/**
 * op_add - adds 2 numbers
 * @a: input number 1
 * @b: input number 2
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 numbers
 * @a: input number 1
 * @b: input number 2
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 numbers
 * @a: input number 1
 * @b: input number 2
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devides 2 numbers
 * @a: input number 1
 * @b: input number 2
 * Return: result of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulos 2 numbers
 * @a: input number 1
 * @b: input number 2
 * Return: remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
